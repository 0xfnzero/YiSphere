class IchingService:
    """易经起卦与卦象查询。"""

    def get_gua_by_id(self, gua_id: int) -> Optional[dict]:
        """按卦序取卦（1-64）。"""
        if 1 <= gua_id <= 64:
            return ICHING_64[gua_id - 1].copy()
        return None

    def draw_random(self) -> dict:
        """随机起一卦（用于占问）。"""
        idx = random.randint(0, 63)
        return ICHING_64[idx].copy()

    def draw_by_seed(self, seed: int) -> dict:
        """按种子起卦，相同种子得相同卦。"""
        idx = (abs(seed) % 64)
        return ICHING_64[idx].copy()


